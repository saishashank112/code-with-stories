# 2. Data Types
# Definition: Python supports different types like int, float, str, bool, and more.

# Scenario: An eCommerce app uses:
# int for product stock count
# float for price
# str for product name
# bool for is_available

product_name = "Laptop"
product_price = 999.99
product_stock = 50
is_available = True
# These variables represent a product in an eCommerce application.
# Example usage
print(product_name)  # Output: Laptop
print(product_price)  # Output: 999.99  
print(product_stock)  # Output: 50
print(is_available)  # Output: True