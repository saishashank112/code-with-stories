# 8. Dictionaries
# Definition: A dictionary stores data in key-value pairs.

# Scenario: A product catalog with name, price, and stock:

product = {"name": "Laptop", "price": 75000, "stock": 12}

print(product)

# # If we want to access the keys in the dictionary
print(product.keys())


